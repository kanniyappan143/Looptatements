package loopstatement;

public class WhileLoopDemo
 	{
	//rules : while loop is sepreate sepreate initial value and while loop condition checking then true inside of body
	//condition when check true print i (increment,decrement)value printed 

	public static void main(String[] args) 
	{
	 int i=1;
	 int j=10;
	 while(i<11)
	 {
		 System.out.println("i value is "+i);
		 i++;
	 }
	 while(j>1)
	 {
		 System.out.println("j value is"+j);
		 j--;
	 }
	 }
	 }
	 
	


